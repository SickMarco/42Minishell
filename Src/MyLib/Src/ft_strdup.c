#include "../mylib.h"

char	*ft_strdup(char *src)
{
	int		i;
	char	*x;

	i = 0;
	while (src[i] != '\0')
		i++;
	x = malloc(sizeof(char) * i + 1);
	if (!x)
		return (NULL);
	i = 0;
	while (src[i] != '\0')
	{
		x[i] = src[i];
		i++;
	}
	x[i] = '\0';
	return (x);
}
